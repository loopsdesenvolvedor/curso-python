# Dados em Python.
# Exemplos.
name = "Top Gun Maverick"
yearLaunch = 2023
noteMovie = 9.5
planIncluded = False

# Função para saber o tipo de dados = type(dado)

print(type(name)) # retorno: <class 'str'>
print(type(yearLaunch)) # retorno: <class 'int'>
print(type(noteMovie)) # retorno: <class 'float'>
print(type(planIncluded)) # retorno: <class 'bool'>